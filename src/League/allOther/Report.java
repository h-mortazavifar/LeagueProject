package League.allOther;

public class Report {
    private String[] mostGoalPlayers;
    private String[] noGoalTeams;
    private String[] matchesWithMostGoal;
    private String[] matchesWithNoGoal;


    public String[] getMostGoalPlayers() {
        return mostGoalPlayers;
    }

    public void setMostGoalPlayers(String[] mostGoalPlayers) {
        this.mostGoalPlayers = mostGoalPlayers;
    }

    public String[] getNoGoalTeams() {
        return noGoalTeams;
    }

    public void setNoGoalTeams(String[] noGoalTeams) {
        this.noGoalTeams = noGoalTeams;
    }

    public String[] getMatchesWithMostGoal() {
        return matchesWithMostGoal;
    }

    public void setMatchesWithMostGoal(String[] matchesWithMostGoal) {
        this.matchesWithMostGoal = matchesWithMostGoal;
    }

    public String[] getMatchesWithNoGoal() {
        return matchesWithNoGoal;
    }

    public void setMatchesWithNoGoal(String[] matchesWithNoGoal) {
        this.matchesWithNoGoal = matchesWithNoGoal;
    }

}
